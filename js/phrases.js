function scorePurchaseCart(items) {
  let good = 0;
  let junk = 0;
  items.forEach((item) => {
    const kind = purchaseItemKind[item.id] || 'neutral';
    if (kind === 'good') good += 1;
    else if (kind === 'junk') junk += 1;
  });
  return { good, junk, total: items.length };
}

export function getPurchaseReviewPhrase(items) {
  const { good, junk, total } = scorePurchaseCart(items);
  const names = items.map((i) => `${i.emoji} ${i.name}`).join(', ');

  if (junk >= 3 || (junk >= 2 && good <= 1)) {
    const line = pickRandom(phrases.purchaseReview.mostlyJunk);
    return `${line} В пакете: ${names}.`;
  }
  if (good >= 3 && junk === 0) {
    const line = pickRandom(phrases.purchaseReview.mostlyGood);
    return `${line} Закуп: ${names}.`;
  }
  if (good > junk) {
    const line = pickRandom(phrases.purchaseReview.mixedGood);
    return `${line} ${names}.`;
  }
  if (junk > good) {
    const line = pickRandom(phrases.purchaseReview.mixedBad);
    return `${line} ${names}.`;
  }
  const line = pickRandom(phrases.purchaseReview.balanced);
  return `${line} ${names}.`;
}

