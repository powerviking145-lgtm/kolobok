export function sumItemEffects(items) {
  const sum = { hunger: 0, thirst: 0, health: 0, mood: 0 };
  items.forEach((item) => {
    const fx = item.effects || {};
    Object.keys(sum).forEach((k) => {
      sum[k] += fx[k] || 0;
    });
  });
  return sum;
}

